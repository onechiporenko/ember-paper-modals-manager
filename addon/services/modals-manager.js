import ModalsManager from 'ember-modals-manager-internal/services/modals-manager';

/**
 * @class ModalsManager
 */
export default class ModalsManagerService extends ModalsManager {

  /**
   * Shows custom modal
   *
   * @method show
   * @param {string} componentName component's name with custom modal
   * @param {object} options
   * @return {RSVP.Promise}
   */

  /**
   * Shows `alert`-modal
   *
   * @method alert
   * @param {object} options
   * @return {RSVP.Promise}
   */

  /**
   * Shows `confirm`-modal
   *
   * @method confirm
   * @param {object} options
   * @return {RSVP.Promise}
   */

  /**
   * Shows `prompt`-modal
   *
   * @method prompt
   * @param {object} options
   * @return {RSVP.Promise}
   */

  /**
   * Shows `prompt-confirm`-modal
   *
   * @method promptConfirm
   * @param {object} options
   * @return {RSVP.Promise}
   */

  /**
   * Show `check-confirm`-modal
   *
   * @method checkConfirm
   * @param {object} options
   * @return {RSVP.Promise}
   */

  /**
   * Shows `progress`-modal
   *
   * @method progress
   * @param {object} options
   * @return {RSVP.Promise}
   */

  /**
   * Shows `process`-modal
   *
   * @method process
   * @param {object} options
   * @return {RSVP.Promise}
   */
}
